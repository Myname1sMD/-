async function startRecording() {
    if (!navigator.mediaDevices || !navigator.mediaDevices.getUserMedia) {
        alert(currentLang === 'ru' ? "–ò–∑–≤–∏–Ω–∏—Ç–µ, –∑–∞–ø–∏—Å—å –≥–æ–ª–æ—Å–∞ –Ω–µ –ø–æ–¥–¥–µ—Ä–∂–∏–≤–∞–µ—Ç—Å—è –≤–∞—à–∏–º –±—Ä–∞—É–∑–µ—Ä–æ–º." : "–ö–µ—à—ñ—Ä—ñ“£—ñ–∑, –¥–∞—É—ã—Å—Ç—ã –∂–∞–∑—É —Å—ñ–∑–¥—ñ“£ –±—Ä–∞—É–∑–µ—Ä—ñ“£—ñ–∑–¥–µ “õ–æ–ª–¥–∞—É —Ç–∞–ø–ø–∞–π–¥—ã.");
        return;
    }

    try {
        const stream = await navigator.mediaDevices.getUserMedia({ audio: true });
        mediaRecorder = new MediaRecorder(stream);
        audioChunks = [];
        
        mediaRecorder.ondataavailable = event => {
            audioChunks.push(event.data);
        };
        
        mediaRecorder.onstop = () => {
            const audioBlob = new Blob(audioChunks, { type: 'audio/webm' });
            
            const voiceMessageText = getTranslation('send_voice_message_ui');
            displayMessage(voiceMessageText, 'user', audioBlob); 
            
            handleChatQuery('__VOICE_MESSAGE_SENT__'); 

            stream.getTracks().forEach(track => track.stop());
        };

        mediaRecorder.start();
        isRecording = true;
        
        voiceButton.textContent = getTranslation('voice_button_stop');
        voiceButton.classList.add('recording');
        chatInput.disabled = true;
        chatSendButton.disabled = true;

    } catch (err) {
        console.error("–û—à–∏–±–∫–∞ –¥–æ—Å—Ç—É–ø–∞ –∫ –º–∏–∫—Ä–æ—Ñ–æ–Ω—É:", err);
        alert(currentLang === 'ru' ? "–ù–µ —É–¥–∞–ª–æ—Å—å –ø–æ–ª—É—á–∏—Ç—å –¥–æ—Å—Ç—É–ø –∫ –º–∏–∫—Ä–æ—Ñ–æ–Ω—É. –ü—Ä–æ–≤–µ—Ä—å—Ç–µ —Ä–∞–∑—Ä–µ—à–µ–Ω–∏—è." : "–ú–∏–∫—Ä–æ—Ñ–æ–Ω“ì–∞ –∫—ñ—Ä—É –º“Ø–º–∫—ñ–Ω –±–æ–ª–º–∞–¥—ã. –†“±“õ—Å–∞—Ç—Ç–∞—Ä–¥—ã —Ç–µ–∫—Å–µ—Ä—ñ“£—ñ–∑.");
    }
}

function stopRecording() {
    if (mediaRecorder && mediaRecorder.state === 'recording') {
        mediaRecorder.stop();
    }
    isRecording = false;
    
    voiceButton.textContent = getTranslation('voice_button_start');
    voiceButton.classList.remove('recording');
    chatInput.disabled = false;
    chatSendButton.disabled = false;
}

