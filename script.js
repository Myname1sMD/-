        if (section.list && section.list.length) {
            contentHTML += '<strong>–ü—Ä–æ–≥—Ä–∞–º–º—ã:</strong><ul>';
            section.list.forEach(item => contentHTML += `<li>${item.program_name} (–ï–ù–¢: ${item.subjects_required.join(' –∏ ')})</li>`);
            contentHTML += '</ul>';
        }

        if (section.requirements) contentHTML += `<p><strong>–¢—Ä–µ–±–æ–≤–∞–Ω–∏—è:</strong> ${section.requirements}</p>`;
        
